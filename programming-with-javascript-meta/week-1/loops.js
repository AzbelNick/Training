// for loop
for (var i = 1; i <= 5; i++) {
    console.log(i);
}; console.log("counting completed :P");

for (var i = 5; i > 0; i--) {
    console.log(i);
}; console.log("countdown completed :3");

//while loop
var i = 1;
while (i <= 5) {
    console.log(i);
    i++;
}; console.log("counting completed ^^");

var i = 5;
while (i > 0) {
    console.log(i);
    i = i - 1;
}; console.log("countdown completed :O");

var year = 2018;
while (year <= 2022) {
    console.log(year);
    year++;
};